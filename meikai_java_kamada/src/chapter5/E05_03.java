/**
 * 論理型の変数にtrueやfalseを代入して、その値を表示するプログラムを作成せよ。
 *
 */
package chapter5;

public class E05_03 {

    public static void main(String[] args) {
        // 論理型の変数を宣言し、trueを代入する。
        boolean booleanTrue = true;
        // 論理型の変数を宣言し、falseを代入する。
        boolean booleanFlase = false;

        // 論理型のtrueを代入された変数を出力する。
        System.out.println("論理型のtrue：" + booleanTrue);
        // 論理型のfalseを代入された変数を出力する。
        System.out.println("論理型のfalse：" + booleanFlase);

    }

}
